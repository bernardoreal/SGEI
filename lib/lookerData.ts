export function getLookerGlobalData() {
  const lines = RAW_GLOBAL_DATA.split('\n').slice(1);
  const data: Record<string, { realizado: number, autorizadas: number }> = {};
  
  lines.forEach(line => {
    if (!line.trim()) return;
    const [base, realizado, autorizadas] = line.split(',');
    data[base] = {
      realizado: parseFloat(realizado) || 0,
      autorizadas: parseFloat(autorizadas) || 0
    };
  });
  
  return data;
}

